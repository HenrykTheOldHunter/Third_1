package com.company;

public class Circle {
    private int rad = 10;
    private int diam = 20;
    private int area = 500;

    public void setRad(int radius){this.rad = rad;}
    public int getRad(){return rad;}

    public void setDiam(int diam){this.diam = diam;}
    public int getDiam(){return diam;}

    public void setArea(int area){this.area = area;}
    public int getArea(){return area;}

}
